                                   beizhu : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																										
																																													
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`dianfeitongji`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='dianfeitongji'){
					this.ruleForm.dianfeitongji = obj[o];
					this.ro.dianfeitongji = true;
					continue;
					}
					if(o=='jiaonarenshu'){
					this.ruleForm.jiaonarenshu = obj[o];
					this.ro.jiaonarenshu = true;
					continue;
					}
					if(o=='zongjine'){
					this.ruleForm.zongjine = obj[o];
					this.ro.zongjine = true;
					continue;
					}
					if(o=='dengjiren'){
					this.ruleForm.dengjiren = obj[o];
					this.ro.dengjiren = true;
					continue;
					}
					if(o=='dengjishijian'){
					this.ruleForm.dengjishijian = obj[o];
					this.ro.dengjishijian = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																										
			// 多级联动参数
																																										
																																	dengjishijianChange(e) {
				this.ruleForm.dengjishijian = e.target.value;
				this.$forceUpdate();
			},
												
																																							
																																							
																																																									
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																								if(this.ruleForm.jiaonarenshu&&(!this.$validate.isIntNumer(this.ruleForm.jiaonarenshu))){
					this.$utils.msg(`缴纳人数应输入整数`);
					return
				}
																																								if(this.ruleForm.zongjine&&(!this.$validate.isIntNumer(this.ruleForm.zongjine))){
					this.$utils.msg(`总金额应输入整数`);
					return
				}
																																																																																																																																if(this.ruleForm.id){
					await this.$api.update(`dianfeitongji`, this.ruleForm);
				}else{
					await this.$api.add(`dianfeitongji`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
